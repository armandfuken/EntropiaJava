
public class TriangularModel {
	private String x;
	private String a;
	private String b;
	private String c;
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	public String getA() {
		return a;
	}
	public void setA(String a) {
		this.a = a;
	}
	public String getB() {
		return b;
	}
	public void setB(String b) {
		this.b = b;
	}
	public String getC() {
		return c;
	}
	public void setC(String c) {
		this.c = c;
	}
	
	
}
